    public boolean verifyFields(){
        String fname=jTextField_name.getText();
        String email=jTextField_email.getText();
        String username=jTextField_username.getText();
        String password=String.valueOf(jPasswordField.getPassword());
        String cpassword=String.valueOf(jPasswordField_c.getPassword());
        String fteam=jTextField_fteam.getText();
        String location=jTextField_location.getText();
        if(fname.trim().equals("") || email.trim().equals("") || username.trim().equals("") || password.trim().equals("") || cpassword.trim().equals("") || fteam.trim().equals("") || location.trim().equals("")){
            JOptionPane.showMessageDialog(null,"One or more fields are empty","error",2);
            return false;
        }
        //check if the two passwords are equal
        else if(!password.equals(cpassword)){
            JOptionPane.showMessageDialog(null,"Passwords don't match","confirm password",2);
            return false;
        }
        //if everything is ok
        else{
            return true;
        }
    }
    //create a function to check if the entered username already exists in the database
    public boolean checkUsername(String username){
        PreparedStatement st;
        ResultSet rs;
        boolean username_exist=false;
        String query ="Select* from users where username=?";
        try {
            st=My_Cnx.getConnection().prepareStatement(query);
            st.setString(1,username);
            rs=st.executeQuery();
            if(rs.next()){
                username_exist=true;
                JOptionPane.showMessageDialog(null,"this user name already exists, choose another username","username failed",2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        return username_exist;
    }
