    private void jButton_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_signupActionPerformed
        String fname=jTextField_name.getText();
        String email=jTextField_email.getText();
        String username=jTextField_username.getText();
        String password=String.valueOf(jPasswordField.getPassword());
        String cpassword=String.valueOf(jPasswordField_c.getPassword());
        String fteam=jTextField_fteam.getText();
        String location=jTextField_location.getText();
        if(verifyFields()){
            if(!checkUsername(username)){
                if(!nothingTyped()){
                PreparedStatement ps;
                ResultSet rs;
                String registerUserQuery="insert into users (name,email,username,password,favourite_team,location) values(?,?,?,?,?,?)";
                try {
                    ps=My_Cnx.getConnection().prepareStatement(registerUserQuery);
                    ps.setString(1,fname);
                    ps.setString(2,email);
                    ps.setString(3,username);
                    ps.setString(4,password);
                    ps.setString(5,fteam);
                    ps.setString(6,location);
                    if(ps.executeUpdate()!=0){
                        JOptionPane.showMessageDialog(null,"account created successfully","success!",2);
                        Login_form log=new Login_form();
                        log.setVisible(true);
                        log.pack();
                        log.setLocationRelativeTo(null);
                        this.dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Error:check your details");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                }
                }
        }
    }//GEN-LAST:event_jButton_signupActionPerformed

