    private void jButton_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_submitActionPerformed
        try {
            String player1 = Verify.player(jTextField_player1.getText());
            String player2 = Verify.player(jTextField_player2.getText());
            String team1 = jTextField_team1.getText();
            String team2 = jTextField_team2.getText();
            String score1 = jTextField_score1.getText();
            String score2 = jTextField_score2.getText();

            if (player1.equals(player2)) {
                throw new Exception("player1 and player2 can not be the same");
            }
            String table = "kick_off";
            String[] fields = {"player1", "player2", "team1", "team2", "score1", "score2"};
            Object[] values = {player1, player2, team1, team2, score1, score2};
            int result = new My_Cnx().insert(team2, fields, values);
            if (result != 0 || result != -1000) {
                String[] options = {"yes", "no"};
                int x = JOptionPane.showOptionDialog(null, "would you like to play another game", "new kick off", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
                if (x == 0) {
                    jTextField_player1.setText("");
                    jTextField_player2.setText("");
                    jTextField_team1.setText("");
                    jTextField_team2.setText("");
                    jTextField_score1.setText("");
                    jTextField_score2.setText("");
                } else {
                    this.dispose();
                    throw new SQLException("failed to insert to DB");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Player_vs_player.class.getName()).log(Level.SEVERE, null, ex);

        } catch (Exception ex) {
            Logger.getLogger(Player_vs_player.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", 2);
        }
    }//GEN-LAST:event_jButton_submitActionPerformed

